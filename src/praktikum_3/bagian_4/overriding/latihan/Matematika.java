package modul_3.bagian_4.overriding.latihan;

class Matematika {
    // Overloading dengan 2 parameter int
    public int tambah(int a, int b) {
        return a + b;
    }

    // Overloading dengan 3 parameter int
    public int tambah(int a, int b, int c) {
        return a + b + c;
    }

    // Overloading dengan 2 parameter double
    public double tambah(double a, double b) {
        return a + b;
    }
}
